#include <libft.h>

void 	ft_delstrtab(char **tab)
{
	int	i;

	if (!tab || !*tab)
		return;
	i = 0;
	while (tab[i])
	{
		free(tab[i]);
		i++;
	}
	free(tab);
	tab = NULL;
}
